#include <iostream>
using namespace std;
 
int main()
{
    int i = 7;
    cout << (++i) << endl;
    // 8 i=8
    cout << (i++) << endl;
    // 8 i=9
    cout << (i--) << endl;
    // 9 i=8
    cout << (--i) << endl;
    // 7 i=7

    // Q1. a=11 1 2 
    // Q2. Stage1 - Inside If
    // a=0 b=3 
    return 0;
}